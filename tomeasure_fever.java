package DSA.conditional_statement;

public class tomeasure_fever {
    public static void main(String[] args) {
        double bodytemp = 103.5;
        double normaltemp = 100;
        if (bodytemp <= normaltemp) {
            System.out.println("you don't have a fever");
        } else {
            System.out.println("yes you have a fever");
        }
    }

}
